namespace SharpDX.Toolkit.Audio
{
    /// <summary>
    /// Defines the number of channels in the audio data.
    /// </summary>
    public enum AudioChannels 
    {
        /// <summary>
        /// Indicates audio data is contained in one channel.
        /// </summary>
        Mono   = 1,
        /// <summary>
        /// Indicates audio data contains two channels.
        /// </summary>
        Stereo = 2
    }
}
